function buildTaskEmotion(task: ClueTask): TaskEmotion {
  const caseEmotion = CASE_EMOTION[task.caseId]
  const isEvidence = task.title.replace(/^Улика\s*№?\d*:\s*/, '').toLowerCase()
  const base = {
    mystery: caseEmotion.mystery,
    hypothesis: `Версия сыщика: ${isEvidence} должна объяснять факт, который можно проверить наблюдением.`,
    proofGoal: TYPE_PROOF_GOAL[task.type],
    worldChange: caseEmotion.worldChange,
    albumBlurb: caseEmotion.albumBlurb,
  }

  return { ...base, ...TASK_EMOTION_OVERRIDES[task.id] }
}

