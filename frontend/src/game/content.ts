export function getEvidenceLinksForCase(caseId: string) {
  const taskIds = new Set(getTasksForCase(caseId).map((t) => t.id))
  return EVIDENCE_LINKS.filter((link) => taskIds.has(link.taskId))
}

export function getEvidenceLinksForTask(taskId: string) {
  return EVIDENCE_LINKS.filter((link) => link.taskId === taskId)
}

export type SuspectStatus = 'cleared' | 'suspicious' | 'unknown'

export function getSuspectStatus(
  suspect: Suspect,
  approvedTaskIds: Set<string>,
  links: EvidenceLink[],
): { status: SuspectStatus; pins: EvidenceLink[] } {
  const relevant = links.filter((link) => link.suspectId === suspect.id && approvedTaskIds.has(link.taskId))
  if (relevant.length === 0) return { status: 'unknown', pins: [] }

  const hasEliminate = relevant.some((link) => link.effect === 'eliminates')
  const hasImplicate = relevant.some((link) => link.effect === 'implicates')

  if (hasImplicate) return { status: 'suspicious', pins: relevant }
  if (hasEliminate) return { status: 'cleared', pins: relevant }
  return { status: 'unknown', pins: relevant }
}
